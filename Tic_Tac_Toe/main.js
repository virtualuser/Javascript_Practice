
var pos1 = document.getElementById("pos1");

pos1.onclick = function() 
{
	alert(pos1.src);
	var trim = pos1.src.substring(64, pos1.src.length);
	if(trim == "empty.bmp")
	{
		pos1.src = 'X.bmp';
	}
	else if(trim == "X.bmp")
	{
		pos1.src = 'O.bmp';
	}
	else if(trim == "O.bmp"){
		pos1.src = 'empty.bmp';
	}
	else{
		pos1.src = 'X.bmp';
	}
};

